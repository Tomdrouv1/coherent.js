#!/usr/bin/env node
/**
 * API Surface Snapshot Tool
 *
 * Walks each workspace package's package.json `exports` field, dynamic-imports
 * each subpath, and snapshots the sorted list of exported symbol names to
 * `packages/<name>/api-surface.txt`.
 *
 * Two modes:
 *   --write   Regenerate all snapshots from current source/dist.
 *   --check   Compare current surface against committed snapshots.
 *             Exits non-zero if any package's surface has drifted.
 *
 * The snapshot is intentionally just symbol names (not type signatures or
 * method surfaces) — a strong, low-maintenance gate. Type-level changes can
 * be added in a future iteration if needed.
 *
 * @module scripts/check-api-surface
 */

import { readdirSync, statSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, join, dirname } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const PACKAGES_DIR = resolve(REPO_ROOT, 'packages');

const HEADER_LINES = [
  '# API Surface Snapshot',
  '# Generated by scripts/check-api-surface.mjs --write',
  '# DO NOT EDIT MANUALLY. To update: run `node scripts/check-api-surface.mjs --write`',
  '# Each section lists the sorted exported symbol names for one subpath.',
  '#',
];

const MODE_WRITE = '--write';
const MODE_CHECK = '--check';

function listPackages() {
  return readdirSync(PACKAGES_DIR)
    .filter((name) => statSync(join(PACKAGES_DIR, name)).isDirectory())
    .map((name) => ({ name, dir: join(PACKAGES_DIR, name) }))
    .filter(({ dir }) => existsSync(join(dir, 'package.json')))
    .map(({ name, dir }) => {
      const pkgJson = JSON.parse(readFileSync(join(dir, 'package.json'), 'utf8'));
      return { name, dir, pkgJson };
    })
    .filter(({ pkgJson }) => pkgJson.exports && Object.keys(pkgJson.exports).length > 0);
}

function resolveExportPath(exportValue) {
  if (typeof exportValue === 'string') {
    if (exportValue.endsWith('/')) return null;
    return exportValue;
  }
  if (exportValue && typeof exportValue === 'object') {
    if (typeof exportValue.import === 'string') return exportValue.import;
    if (typeof exportValue.default === 'string') return exportValue.default;
    if (typeof exportValue.require === 'string') return exportValue.require;
  }
  return null;
}

async function snapshotSubpath(packageDir, subpath, exportValue) {
  const relPath = resolveExportPath(exportValue);
  if (!relPath) {
    return { subpath, exports: null, note: 'directory export (no snapshot)' };
  }
  const absPath = resolve(packageDir, relPath);
  if (!existsSync(absPath)) {
    return { subpath, exports: null, note: `target file missing: ${relPath}` };
  }
  try {
    const mod = await import(pathToFileURL(absPath).href);
    const names = Object.keys(mod).sort();
    return { subpath, exports: names, note: null };
  } catch (err) {
    const tag = (err && err.code) || (err && err.name) || 'unknown';
    return { subpath, exports: null, note: `import failed (${tag})` };
  }
}

function formatSnapshot(packageName, sections) {
  const lines = [
    ...HEADER_LINES,
    `# Package: ${packageName}`,
    '',
  ];
  for (const { subpath, exports, note } of sections) {
    lines.push(`== ${subpath} ==`);
    if (note) {
      lines.push(`# ${note}`);
    } else if (exports.length === 0) {
      lines.push('# (no exports)');
    } else {
      for (const name of exports) {
        lines.push(name);
      }
    }
    lines.push('');
  }
  return lines.join('\n');
}

async function snapshotPackage(pkg) {
  const subpaths = Object.keys(pkg.pkgJson.exports).sort();
  const sections = [];
  for (const subpath of subpaths) {
    const section = await snapshotSubpath(pkg.dir, subpath, pkg.pkgJson.exports[subpath]);
    sections.push(section);
  }
  const missingTargets = sections
    .filter(({ note }) => note && note.startsWith('target file missing'))
    .map(({ subpath, note }) => `${subpath}: ${note}`);
  const phantomTypes = checkTypesParity(pkg, sections);
  return { name: pkg.name, dir: pkg.dir, missingTargets, phantomTypes, content: formatSnapshot(`@coherent.js/${pkg.name}`, sections) };
}

/**
 * Extract value-export names declared in a .d.ts file.
 * Only definitive value declarations (function/const/class/let/var) are
 * returned — brace re-exports may cover interfaces and are intentionally
 * excluded so type-only names can't be misread as runtime values.
 */
function parseDtsValueExports(dtsPath) {
  const source = readFileSync(dtsPath, 'utf8');
  const names = new Set();
  const patterns = [
    /^export\s+(?:declare\s+)?(?:async\s+)?function\s+([A-Za-z_$][\w$]*)/gm,
    /^export\s+(?:declare\s+)?(?:const|let|var)\s+([A-Za-z_$][\w$]*)/gm,
    /^export\s+(?:declare\s+)?(?:abstract\s+)?class\s+([A-Za-z_$][\w$]*)/gm,
  ];
  for (const re of patterns) {
    let m;
    while ((m = re.exec(source)) !== null) names.add(m[1]);
  }
  return names;
}

function resolveTypesPath(pkg, subpath, exportValue) {
  if (exportValue && typeof exportValue === 'object' && typeof exportValue.types === 'string') {
    return resolve(pkg.dir, exportValue.types);
  }
  if (subpath === '.' && typeof pkg.pkgJson.types === 'string') {
    return resolve(pkg.dir, pkg.pkgJson.types);
  }
  return null;
}

/**
 * Types-parity gate: every value export declared in a subpath's .d.ts must
 * exist at runtime. Phantom declarations let broken consumer code typecheck
 * and then crash (`createI18n is not a function`), so they are a hard failure.
 */
function checkTypesParity(pkg, sections) {
  const phantoms = [];
  for (const section of sections) {
    if (!section.exports) continue; // no runtime snapshot to compare against
    const exportValue = pkg.pkgJson.exports[section.subpath];
    const typesPath = resolveTypesPath(pkg, section.subpath, exportValue);
    if (!typesPath || !existsSync(typesPath)) continue;
    const declared = parseDtsValueExports(typesPath);
    const runtime = new Set(section.exports);
    for (const name of declared) {
      if (!runtime.has(name)) {
        phantoms.push(`${section.subpath}: \`${name}\` declared in ${typesPath.replace(REPO_ROOT + '/', '')} but not exported at runtime`);
      }
    }
  }
  return phantoms;
}

function reportPhantomTypes(results) {
  const broken = results.filter(({ phantomTypes }) => phantomTypes && phantomTypes.length > 0);
  if (broken.length === 0) return false;
  console.error('❌ Phantom type declarations (typed but missing at runtime):');
  for (const { name, phantomTypes } of broken) {
    for (const entry of phantomTypes) {
      console.error(`  - ${name}: ${entry}`);
    }
  }
  return true;
}

// A subpath whose target file doesn't exist is always a hard failure — it
// means the published package would ship a broken export. Reported in both
// modes so --write can't bake breakage into the baselines.
function reportMissingTargets(results) {
  const broken = results.filter(({ missingTargets }) => missingTargets.length > 0);
  if (broken.length === 0) return false;
  console.error('❌ Export targets missing (build the packages first, or fix the exports map):');
  for (const { name, missingTargets } of broken) {
    for (const entry of missingTargets) {
      console.error(`  - ${name}: ${entry}`);
    }
  }
  return true;
}

async function runWrite() {
  const packages = listPackages();
  console.log(`📸 Generating API surface snapshots for ${packages.length} packages...`);
  const results = [];
  for (const pkg of packages) {
    const result = await snapshotPackage(pkg);
    results.push(result);
    const target = join(result.dir, 'api-surface.txt');
    writeFileSync(target, result.content, 'utf8');
    console.log(`  ✓ ${result.name}: wrote ${target.replace(REPO_ROOT + '/', '')}`);
  }
  const failed = reportMissingTargets(results) | reportPhantomTypes(results);
  if (failed) {
    process.exitCode = 1;
    return;
  }
  console.log('Done. Review the diffs before committing.');
}

async function runCheck() {
  const packages = listPackages();
  console.log(`🔒 Checking API surface against committed snapshots (${packages.length} packages)...`);
  const failures = [];
  const results = [];
  for (const pkg of packages) {
    const result = await snapshotPackage(pkg);
    results.push(result);
    const { name, dir, content } = result;
    const target = join(dir, 'api-surface.txt');
    if (!existsSync(target)) {
      failures.push({ name, reason: `missing baseline: ${target.replace(REPO_ROOT + '/', '')}` });
      continue;
    }
    const baseline = readFileSync(target, 'utf8');
    if (baseline !== content) {
      failures.push({ name, reason: `drift detected — re-run \`node scripts/check-api-surface.mjs --write\` and commit the updated ${target.replace(REPO_ROOT + '/', '')}` });
    }
  }
  const hasMissingTargets = reportMissingTargets(results);
  const hasPhantomTypes = reportPhantomTypes(results);
  if (failures.length === 0) {
    if (hasMissingTargets || hasPhantomTypes) {
      process.exitCode = 1;
      return;
    }
    console.log('✅ All API surfaces match committed snapshots.');
    return;
  }
  console.error('❌ API surface drift detected:');
  for (const { name, reason } of failures) {
    console.error(`  - ${name}: ${reason}`);
  }
  console.error('');
  console.error('If the changes are intentional, run --write to regenerate the snapshots, review the diff, and commit them together with the code change.');
  process.exitCode = 1;
}

async function main() {
  const mode = process.argv[2];
  if (mode === MODE_WRITE) {
    await runWrite();
  } else if (mode === MODE_CHECK) {
    await runCheck();
  } else {
    console.error('Usage: node scripts/check-api-surface.mjs (--write|--check)');
    console.error('');
    console.error('  --write   Regenerate all packages/<name>/api-surface.txt snapshots.');
    console.error('  --check   Verify current API surface matches committed snapshots.');
    process.exitCode = 2;
  }
}

main().catch((err) => {
  console.error('Unexpected error:', err);
  process.exitCode = 3;
});
